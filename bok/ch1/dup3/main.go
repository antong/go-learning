// Dup3 prints the count and text of lines that
// appear more than once in the named input files.
package main

import (
	"fmt"
	"io/ioutil"
	"os"
	"strings"
)

func main() {
	counts := make(map[string]int)         //lag en map 'counts'
	for _, filename := range os.Args[1:] { //range over args gitt som input (de er en slice)
		data, err := ioutil.ReadFile(filename) //les fil, og legg innholdet i data som er []byte
		if err != nil {                        //hvis feil
			fmt.Fprintf(os.Stderr, "dup3: %v\n", err)
			continue
		}
		for _, line := range strings.Split(string(data), "\n") {
			counts[line]++
		}
	}
	for line, n := range counts {
		if n > 1 {
			fmt.Printf("%d\t%s\n", n, line)
		}
	}
}

